class Event {
  final String title;
  final String date;
  final String location;
  final double price;
  final String category;
  final String imageUrl;
  final String description;
  Event({
    required this.title,
    required this.date,
    required this.location,
    required this.price,
    required this.category,
    required this.imageUrl,
    required this.description,
  });
}
